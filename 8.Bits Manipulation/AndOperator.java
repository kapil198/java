public class AndOperator {
    public static void main(String args[]) {
        // AND Operator
        System.out.println(5 & 6);
        // OR Operator
        System.out.println(5 | 6);
        // XOR Operator
        System.out.println(5 ^ 6);
        // One's Compliment
        System.out.println(~5);
        // Left Shift <<
        System.out.println(5 << 3);
        // Right shift<<
        System.out.println(5 >> 3);

    }
}